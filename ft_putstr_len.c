#include "printf.h"

int	ft_putstr_len(char *s)
{
	int	i;
	int	len;
	
	if (!s)
		return ;
	i = 0;
	len = 0;
	while (s[i])
	{
		write (1, &s[i], 1);
		i++;
		len++;
	}
	return(len);
}
